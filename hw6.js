function sumOfNonMultiplesOfThree() {
    let sum = 0; // Ініціалізуємо змінну для зберігання суми чисел

    for (let i = 1; i <= 100; i++) {
        // Перевіряємо, чи поточне число не кратне 3
        if (i % 3 !== 0) {
            sum += i; // Додаємо число до суми, якщо воно не кратне 3
        }
    }

    return sum; // Повертаємо обчислену суму
}

// Виведення суми чисел, які не кратні 3, від 1 до 100
console.log(sumOfNonMultiplesOfThree());
